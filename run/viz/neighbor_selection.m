%% run_test_plot_reference_same_side_skew_neighbors
clear; clc;
close all;

rootFolder = fullfile(getenv('HOME'), 'Desktop', 'Strain Spectra', 'structures');
filename   = fullfile(rootFolder, 'a_0.0_CONTCAR.vasp');

%% ------------------------------------------------------------------------
% Import crystal template
crystal = io.import_contcar_as_crystal(filename, ...
    'BondScale', 1.20, ...
    'WrapFractional', true, ...
    'SortMolecules', false);

fprintf('Imported crystal template:\n');
fprintf('  nSites     = %d\n', size(crystal.cart_coords, 1));
fprintf('  nBaseMols  = %d\n', numel(unique(crystal.base_mol_id)));

%% ------------------------------------------------------------------------
% Minimal model
model = struct();

model.polarizable_classes = { ...
    'H_on_C_deg3', ...
    'H_on_C_deg4', ...
    'C_deg3', ...
    'C_deg4', ...
    'N', ...
    'O'};

model.alpha_by_class = struct( ...
    'H_on_C_deg3', 0.696, ...
    'H_on_C_deg4', 0.696, ...
    'C_deg3',      1.750, ...
    'C_deg4',      1.750, ...
    'N',           1.073, ...
    'O',           0.837);

model.thole_a = 0.39;

%% ------------------------------------------------------------------------
% Working options
opts = struct();
opts.supercell_size = [2 5 1];   % adjust as needed
opts.bondScale = 1.20;
opts.verbose = true;
opts.removeMolIDs = [];
opts.activeMolIDs = [];

%% ------------------------------------------------------------------------
% Build working system
sys = builder.make_crystal_system(crystal, model, opts);

fprintf('\nBuilt working system:\n');
fprintf('  n_unit_sites = %d\n', sys.n_unit_sites);
fprintf('  n_cells      = %d\n', sys.n_cells);
fprintf('  n_sites      = %d\n', sys.n_sites);

%% ------------------------------------------------------------------------
% Complete-molecule summary
completeIDs = builder.complete_molecule_ids(sys, 'Verbose', true);

if isempty(completeIDs)
    error('No complete molecules available in displayed supercell.');
end

%% ------------------------------------------------------------------------
% Best-centered same-stack pair
pair_same = builder.select_centered_neighbor_pair(sys, ...
    'Relation', 'same_stack', ...
    'Shell', 1, ...
    'StackAxis', 'b', ...
    'PerpTol', 1e-2, ...
    'ShellTol', 1e-2, ...
    'Direction', 'either', ...
    'Verbose', true);

fprintf('\nChosen same-stack pair:\n');
fprintf('  ref ID      = %d\n', pair_same.reference_mol_id);
fprintf('  nbr ID      = %d\n', pair_same.neighbor_mol_id);
fprintf('  midpoint d  = %.4f\n', pair_same.midpoint_distance);

%% ------------------------------------------------------------------------
% Best-centered side-stack pair
pair_side = builder.select_centered_neighbor_pair(sys, ...
    'Relation', 'side_stack', ...
    'Shell', 1, ...
    'Member', 1, ...
    'StackAxis', 'b', ...
    'SameStackPerpTol', 1e-2, ...
    'VectorGroupTol', 1e-2, ...
    'ShellTol', 1e-2, ...
    'Direction', 'either', ...
    'Verbose', true);

fprintf('\nChosen side-stack pair:\n');
fprintf('  ref ID      = %d\n', pair_side.reference_mol_id);
fprintf('  nbr ID      = %d\n', pair_side.neighbor_mol_id);
fprintf('  midpoint d  = %.4f\n', pair_side.midpoint_distance);

%% ------------------------------------------------------------------------
% Best-centered skew pair
pair_skew = [];
try
    pair_skew = builder.select_centered_neighbor_pair(sys, ...
        'Relation', 'skew', ...
        'Shell', 2, ...
        'StackAxis', 'b', ...
        'NormalTargetDeg', 72, ...
        'NormalTolDeg', 15, ...
        'ShellTol', 1e-2, ...
        'Direction', 'either', ...
        'Verbose', true);

    fprintf('\nChosen skew pair:\n');
    fprintf('  ref ID      = %d\n', pair_skew.reference_mol_id);
    fprintf('  nbr ID      = %d\n', pair_skew.neighbor_mol_id);
    fprintf('  midpoint d  = %.4f\n', pair_skew.midpoint_distance);
catch ME
    warning('Skew pair selection failed: %s', ME.message);
end

%% ------------------------------------------------------------------------
% Plot same-stack pair
fig1 = figure(1);
clf(fig1);
fig1.Color = 'w';
ax1 = axes('Parent', fig1);

viz.plot_supercell_selection(sys, ...
    'ReferenceMolID', pair_same.reference_mol_id, ...
    'NeighborMolID', pair_same.neighbor_mol_id, ...
    'RequireCompleteRef', true, ...
    'RequireCompleteNbr', true, ...
    'IncompleteAction', 'error', ...
    'EnvMarkerSize', 52, ...
    'RefMarkerSize', 78, ...
    'NbrMarkerSize', 78, ...
    'ShowCOM', true, ...
    'ShowLabels', true, ...
    'DrawBox', true, ...
    'Axes', ax1, ...
    'Title', sprintf('Best-centered same-stack pair: %d / %d', ...
        pair_same.reference_mol_id, pair_same.neighbor_mol_id));

%% ------------------------------------------------------------------------
% Plot side-stack pair
fig2 = figure(2);
clf(fig2);
fig2.Color = 'w';
ax2 = axes('Parent', fig2);

viz.plot_supercell_selection(sys, ...
    'ReferenceMolID', pair_side.reference_mol_id, ...
    'NeighborMolID', pair_side.neighbor_mol_id, ...
    'RequireCompleteRef', true, ...
    'RequireCompleteNbr', true, ...
    'IncompleteAction', 'error', ...
    'EnvMarkerSize', 52, ...
    'RefMarkerSize', 78, ...
    'NbrMarkerSize', 78, ...
    'ShowCOM', true, ...
    'ShowLabels', true, ...
    'DrawBox', true, ...
    'Axes', ax2, ...
    'Title', sprintf('Best-centered side-stack pair: %d / %d', ...
        pair_side.reference_mol_id, pair_side.neighbor_mol_id));

%% ------------------------------------------------------------------------
% Plot skew pair, if found
if ~isempty(pair_skew)
    fig3 = figure(3);
    clf(fig3);
    fig3.Color = 'w';
    ax3 = axes('Parent', fig3);

    viz.plot_supercell_selection(sys, ...
        'ReferenceMolID', pair_skew.reference_mol_id, ...
        'NeighborMolID', pair_skew.neighbor_mol_id, ...
        'RequireCompleteRef', true, ...
        'RequireCompleteNbr', true, ...
        'IncompleteAction', 'error', ...
        'EnvMarkerSize', 52, ...
        'RefMarkerSize', 78, ...
        'NbrMarkerSize', 78, ...
        'ShowCOM', true, ...
        'ShowLabels', true, ...
        'DrawBox', true, ...
        'Axes', ax3, ...
        'Title', sprintf('Best-centered skew pair: %d / %d', ...
            pair_skew.reference_mol_id, pair_skew.neighbor_mol_id));
end

fprintf('\nCheckpoint passed.\n');